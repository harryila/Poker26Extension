#!/usr/bin/env bash
# =============================================================================
# Pot-odds + EV analysis across the full small-tier grid (NON-pilot cells).
# =============================================================================
#
# Per-cell output:
#   results/pot_odds_full_grid/<cell>.csv             (one row per decision)
#   results/pot_odds_full_grid/<cell>.summary.json    (cell-level aggregates)
#   results/pot_odds_full_grid/<cell>.stdout          (run log)
#
# Aggregate:
#   results/pot_odds_full_grid/SUMMARY.md             (cross-cell comparison
#                                                       generated by aggregate
#                                                       step at the end)
#
# What's covered:
#   - 18 CoT cells       (cot_<model>_t{0,02}_s{42,123,456}_informative_v2)
#   - 18 baseline cells  (scaled_<model>_t{0,02}_s{42,123,456}_informative_v2)
#   - 3 models           (llama8b, qwen8b, ministral8b)
#   PILOT cells are intentionally excluded -- the full-grid cells supersede
#   them and we don't want them double-counted in the cross-cell aggregate.
#
# Cost (CPU-bound, no GPU needed):
#   - ~0.5-1.0s per decision @ --num-rollouts 30 --samples-per-bucket 2
#     --skip-belief-ev (matches the 4-cell Ministral pre-run)
#   - 36 cells x ~500 decisions = ~18,000 decisions
#   - PARALLEL=4 default -> ~10-15 min wall-clock on a Mac M-series
#   - PARALLEL=8 (set via env var) -> ~5-8 min if your box can spare cores
#
# Resume:
#   - Idempotent: any cell whose CSV already exists is skipped.
#   - Delete the CSV (and .summary.json) of a cell to re-run that cell.
#
# Usage:
#   bash scripts/run_pot_odds_full_grid.sh
#   PARALLEL=8 bash scripts/run_pot_odds_full_grid.sh
#   PARALLEL=1 bash scripts/run_pot_odds_full_grid.sh   # serial, easier to debug
# =============================================================================

set -euo pipefail

LOGS_DIR="${LOGS_DIR:-logs}"
RESULTS_DIR="${RESULTS_DIR:-results/pot_odds_full_grid}"
PARALLEL="${PARALLEL:-4}"
NUM_ROLLOUTS="${NUM_ROLLOUTS:-30}"
SAMPLES_PER_BUCKET="${SAMPLES_PER_BUCKET:-2}"
OPPONENT_PRESET="${OPPONENT_PRESET:-informative_v2}"

mkdir -p "$RESULTS_DIR"

cd "$(dirname "$0")/.."  # repo root (xpoker2026Extension/)

# Activate venv if available (so pot_odds_analysis can find pandas etc.).
if [[ -f "../venv/bin/activate" ]]; then
    # shellcheck disable=SC1091
    source ../venv/bin/activate
elif [[ -f "venv/bin/activate" ]]; then
    # shellcheck disable=SC1091
    source venv/bin/activate
fi

# ---------------------------------------------------------------------------
# Build the cell list. (Portable to macOS bash 3.2: no mapfile.)
# ---------------------------------------------------------------------------
INPUTS=()
while IFS= read -r line; do
    [[ -n "$line" ]] && INPUTS+=("$line")
done < <(ls "$LOGS_DIR"/cot_*_enriched.jsonl* "$LOGS_DIR"/scaled_*_enriched.jsonl* 2>/dev/null \
    | grep -v "_pilot_" | sort)

n_total=${#INPUTS[@]}
n_already=0
TODO=()
for inp in "${INPUTS[@]}"; do
    base="$(basename "$inp" .gz)"
    base="${base%.jsonl}"            # cot_llama8b_t0_s42_informative_v2_enriched
    cell="${base%_enriched}"          # cot_llama8b_t0_s42_informative_v2
    cell="${cell%_${OPPONENT_PRESET}}"  # cot_llama8b_t0_s42
    csv="${RESULTS_DIR}/${cell}.csv"
    if [[ -f "$csv" ]]; then
        n_already=$((n_already + 1))
        continue
    fi
    TODO+=("$inp::$cell")
done

echo "Pot-odds full-grid driver"
echo "  inputs:        $n_total enriched logs (cot + scaled, no pilots)"
echo "  already done:  $n_already"
echo "  to run:        ${#TODO[@]}"
echo "  parallel:      $PARALLEL"
echo "  rollouts:      $NUM_ROLLOUTS  samples/bucket: $SAMPLES_PER_BUCKET"
echo "  results dir:   $RESULTS_DIR"
echo

# ---------------------------------------------------------------------------
# Worker (inline; portable to macOS bash 3.2 -- no exported functions for
# xargs to pick up, no GNU parallel dependency).
# ---------------------------------------------------------------------------
run_one() {
    local spec="$1"
    local inp="${spec%%::*}"
    local cell="${spec##*::}"
    local csv="${RESULTS_DIR}/${cell}.csv"
    local stdout_log="${RESULTS_DIR}/${cell}.stdout"

    local started
    started=$(date +%s)
    echo "  [start]  $cell  ($(basename "$inp"))"

    python -m analysis.pot_odds_analysis \
        --input "$inp" \
        --output "$csv" \
        --opponent "$OPPONENT_PRESET" \
        --num-rollouts "$NUM_ROLLOUTS" \
        --samples-per-bucket "$SAMPLES_PER_BUCKET" \
        --skip-belief-ev \
        > "$stdout_log" 2>&1 \
        && { echo "  [done]   $cell  in $(( $(date +%s) - started ))s"; } \
        || { echo "  [FAIL]   $cell  (see $stdout_log)"; return 1; }
}

# ---------------------------------------------------------------------------
# Parallel dispatch via bash background jobs + slot wait.
# ---------------------------------------------------------------------------
running_pids=()
wait_for_slot() {
    local max="$1"
    while [[ ${#running_pids[@]} -ge $max ]]; do
        local survivors=()
        for pid in "${running_pids[@]}"; do
            if kill -0 "$pid" 2>/dev/null; then
                survivors+=("$pid")
            else
                wait "$pid" 2>/dev/null || true
            fi
        done
        running_pids=("${survivors[@]}")
        if [[ ${#running_pids[@]} -ge $max ]]; then
            sleep 0.5
        fi
    done
}

if [[ ${#TODO[@]} -gt 0 ]]; then
    if [[ "$PARALLEL" == "1" ]]; then
        for spec in "${TODO[@]}"; do run_one "$spec"; done
    else
        for spec in "${TODO[@]}"; do
            wait_for_slot "$PARALLEL"
            run_one "$spec" &
            running_pids+=($!)
        done
        wait
    fi
fi

echo
echo "Pot-odds runs complete. Aggregating..."

